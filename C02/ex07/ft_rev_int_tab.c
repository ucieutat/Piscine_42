#include <unistd.h>

void	ft_swap(int *a, int *b)
{
	int	c;

	c = *a;
	*a = *b;
	*b = c;
}

void	ft_rev_int_tab(int *tab, int size)
{
	int	swp;

	swp = 0;
	while (swp < size)
		ft_swap(&tab[swp++], &tab[--size]);
}
