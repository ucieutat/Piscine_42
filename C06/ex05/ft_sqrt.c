int	ft_sqrt(int nb)
{
	unsigned int	i;

	i = 0;
	if (nb < 0)
		return (0);
	while (i * i < (unsigned)nb)
		i++;
	if (i * i == (unsigned)nb)
		return (i);
	else
		return (0);
}
