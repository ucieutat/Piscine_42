#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

char	*ft_strcpy(char *dest, char *src)
{
	if (*src == '\0')
	{
		*dest = *src;
		return (dest);
	}
	else
	{
		*dest = *src;
		ft_strcpy(dest + 1, src + 1);
	}
	return (dest);
}

char	*ft_strdup(char *src)
{
	char	*dest;

	dest = malloc((ft_strlen(src) + 1) * sizeof(char));
	if (!dest)
		return (dest);
	return (ft_strcpy(dest, src));
}
